package com.framed.cdss.utils;
/**
 * Enumerates the directions of trends:
 * <ul>
 *   <li>{@link #UP}: find upwards trends</li>
 *   <li>{@link #DOWN}: find downwards trends</li>
 *   <li>{@link #BOTH}: find both up- and downwards trends</li>
 * </ul>
 */
public enum TrendDirection { UP, DOWN, BOTH }

